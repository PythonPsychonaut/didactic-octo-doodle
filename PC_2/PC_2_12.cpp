/*
Diamond Pattern
Write a program that displays the following pattern on the screen:
   *
  ***
 ***** 
*******
 *****
  ***
   *
*/
// This program displays a diamond pattern on the screen.
#include <iostream>
using namespace std;

int main()
{
	cout << "   *\n";
	cout << "  ***\n";
	cout << " *****\n";
	cout << "*******\n";
	cout << " *****\n";
	cout << "  ***\n";
	cout << "   *\n";
	return 0;
}